public class KernelCalculatorr {

    public KernelCalculatorr() {
    }
    
     public static double Multiplicacao(double a,double b) {
        double x = 0;
        x = a * b;
        return x;  
    }
    
    public static double Divisao(double a,double b) {
        double x = 0;
        x = a / b;
        return x;  
    }
    
    public static double Subtracao(double a,double b) {
        double x = 0;
        x = a - b;
        return x;
    }
    
    public static double Adicao(double z,double y) {
        double x = 0;
        x = z + y;
        return x;  
    }
    
    
    
}

